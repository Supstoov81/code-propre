package fr.digi.ex1;

/**
 * La classe {@code Calcul} fournit des méthodes pour effectuer des opérations arithmétiques de base.
 */
public class Calcul {

	/**
	 * Effectue l'addition de deux nombres entiers.
	 *
	 * @param a le premier nombre entier
	 * @param b le deuxième nombre entier
	 * @return la somme de {@code a} et {@code b}
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Effectue la soustraction de deux nombres entiers.
	 *
	 * @param a le premier nombre entier
	 * @param b le deuxième nombre entier
	 * @return la différence entre {@code a} et {@code b}
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}

